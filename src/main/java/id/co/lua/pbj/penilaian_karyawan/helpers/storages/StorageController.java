    @GetMapping("/{type}/{fileName:.+}")
    public ResponseEntity<InputStreamResource> getFile(HttpServletRequest request,
                                                       @PathVariable("fileName") String fileName,
                                                       @PathVariable("type") String type) throws IOException {
        //Authorized user will download the file
//        ClassPathResource file = new ClassPathResource("uploads/"+type+"/"+ fileName);
        String path = new File("src/main/resources/uploads/"+type+"/"+ fileName).getAbsolutePath();
        File resource = new File(path);
        InputStream targetStream = new FileInputStream(resource);
        HttpHeaders headers = new HttpHeaders();
        FileNameMap mimeTypes = URLConnection.getFileNameMap();
        String contentType = mimeTypes.getContentTypeFor(fileName);
        if (contentType == null) {
            String extension = fileName.substring(fileName.lastIndexOf('.') + 1, fileName.length());;
            contentType = fileExtensionMap.get(extension);
            if(fileName.contains(".rar")||fileName.contains(".zip")){
                contentType = "application/octet-stream";
            }
        }
        if(fileName.contains(".rar")||fileName.contains(".zip")){
            headers.add("Content-type",contentType);
        }else {
            headers.setContentType(MediaType.parseMediaType(contentType));
        }
        headers.add("Access-Control-Allow-Origin", "*");
        headers.add("Access-Control-Allow-Methods", "GET, POST, PUT");
        headers.add("Access-Control-Allow-Headers", "Content-Type");
        headers.add("Content-Disposition", "filename=" + fileName);
        headers.add("Cache-Control", "no-cache, no-store, must-revalidate");
        headers.add("Pragma", "no-cache");
        headers.add("Expires", "0");

        headers.setContentLength(resource.length());
        ResponseEntity<InputStreamResource> response = new ResponseEntity<InputStreamResource>(
                new InputStreamResource(targetStream), headers, HttpStatus.OK);
        return response;
    }
